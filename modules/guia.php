                        de la zona restringida. A principios de la década de 1970, el gobierno de México quería fomentar
                        la inversión extranjera y comenzó a permitir que los extranjeros compraran propiedades costeras
                        a través de un “fideicomiso” o contrato de fideicomiso. La propiedad costera está en fideicomiso
                        de un banco mexicano. El banco actúa en nombre del comprador extranjero, tomando el título de
                        propiedad del inmueble. El banco entonces tiene la obligación fiduciaria de seguir las
                        instrucciones dadas por el comprador extranjero. Contrariamente a un arrendamiento de tierras,
                        como beneficiario del fideicomiso, el comprador extranjero disfruta de todos los derechos de
                        dominio pleno y puede vender, regalar, arrendar, agregar construcciones, mejorar los edificios
                        existentes, hipotecar y gravar la propiedad. El fideicomiso cumple técnicamente con las
                        prohibiciones de la constitución federal mexicana contra la propiedad extranjera de propiedades
                        en zonas restringidas. Los extranjeros han estado comprando, poseyendo y vendiendo propiedades
